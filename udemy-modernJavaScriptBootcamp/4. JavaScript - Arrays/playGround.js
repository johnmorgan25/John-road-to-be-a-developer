// This is a playground for javascript arrays 

//concat 
let testArray = ['a','b','c',4,5,6];
testArray = testArray.concat(['x']);
console.log(testArray);
// Output:
// ['a','b','c',4,5,6,'x']


//join
testArray = ['a','b','c',4,5,6];
testArray = testArray.join('-');
console.log(testArray);
// Output:
// a-b-c-4-5-6
