// This challeng is meant to build a Temprature converter
// It takes values in Fahrenheit and convert it to both celsius and kelvin

let fahrenheit = 50;

// calculating temprature in celsius
let celsius = (fahrenheit - 32) * (5 / 9);

// calculating temprature in Kelvin
let kelvin = (fahrenheit + 459.67) * (5 / 9);

// printing values
console.log("The temprature in celsius: ", celsius);
console.log("The temprature in kelvin: ", kelvin);
